import java.util.Scanner;

public class Task3 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        byte a = 100;                  // Example of byte
        short b = 3000;                // Example of short
        int c = 50000;                 // Example of int
        long d = 10000000000L;         // Example of long
        float e = 5.75f;               // Example of float
        double f = 19.99;              // Example of double
        char g = 'A';                  // Example of char
        boolean h = true;              // Example of boolean

        System.out.println("Byte Value: " + a);
        System.out.println("Short Value: " + b);
        System.out.println("Int Value: " + c);
        System.out.println("Long Value: " + d);
        System.out.println("Float Value: " + e);
        System.out.println("Double Value: " + f);
        System.out.println("Char Value: " + g);
        System.out.println("Boolean Value: " + h);

        
    }
}
